op=input ("Enter the the operation u want to perform,'+','-','*','/'")
a=float(input("Enter the number :-"))
b=float(input("Enter the number :-"))

if op=='+':
    print(a+b)
elif op=='-':
    print(a-b)
elif op=='*':
    print(a*b)
elif op=='/':
    print(a/b)
else:
    print("wrong operation selected")

     
